package com.achhabra.finworld.beans;

public class BasicCalculatorBean {
	
	private int currentAge;
	
	private double incomeAmount;
	
	private double incomeInflation;
	
	private int incomeToYear;
	
	private double expenseAmount;
	
	private double expenseInflation;
	
	private int expenseToYear;

	public int getCurrentAge() {
		return currentAge;
	}

	public void setCurrentAge(int currentAge) {
		this.currentAge = currentAge;
	}

	public double getIncomeAmount() {
		return incomeAmount;
	}

	public void setIncomeAmount(double incomeAmount) {
		this.incomeAmount = incomeAmount;
	}

	public double getIncomeInflation() {
		return incomeInflation;
	}

	public void setIncomeInflation(double incomeInflation) {
		this.incomeInflation = incomeInflation;
	}

	public int getIncomeToYear() {
		return incomeToYear;
	}

	public void setIncomeToYear(int incomeToYear) {
		this.incomeToYear = incomeToYear;
	}

	public double getExpenseAmount() {
		return expenseAmount;
	}

	public void setExpenseAmount(double expenseAmount) {
		this.expenseAmount = expenseAmount;
	}

	public double getExpenseInflation() {
		return expenseInflation;
	}

	public void setExpenseInflation(double expenseInflation) {
		this.expenseInflation = expenseInflation;
	}

	public int getExpenseToYear() {
		return expenseToYear;
	}

	public void setExpenseToYear(int expenseToYear) {
		this.expenseToYear = expenseToYear;
	}

}
